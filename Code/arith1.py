class Arith1Times:
    def __init__(self,a,b):
        self.a = a
        self.b = b
             
    def __str__(self):
        print(self.a + " x " + self.b)
            
class Arith1Divide:
    def __init__(self,a,b):
        self.a = a
        self.b = b
             
    def __str__(self):
        print(self.a + " / " + self.b)
    
class Arith1Plus:
    def __init__(self,a,b):
        self.a = a
        self.b = b
             
    def __str__(self):
        print(self.a + " +" + self.b)
   
class Arith1Minus:
    def __init__(self,a,b):
        self.a = a
        self.b = b
             
    def __str__(self):
        print(self.a + " +" + self.b)
                    